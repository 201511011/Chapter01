
public class Relation {
	public static void main(String[] args) {
		int a=10;
		int b=3;
		System.out.println(10>3);
		System.out.println(10<3);
		System.out.println(10>=3);
		System.out.println(10<=3);
		System.out.println(10==3);
		System.out.println(10!=3);
		boolean k = a<b;
		System.out.println(k);
		System.out.println((10>3) && (10<3));
		System.out.println(10>3 && 10<3);
		System.out.println(!(10>3));
		
		boolean x=7==9;
		System.out.println("  "+x);
	}
}
